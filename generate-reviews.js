function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

const services = [
  { key: '소형이사', short: moveShort, long: moveLong },
  { key: '입주청소', short: cleanShort, long: cleanLong },
  { key: '용달', short: ydShort, long: ydLong },
  { key: '에어컨청소', short: acShort, long: acLong },
];

const reviews = [];
const perService = 500;

for (const svc of services) {
  for (let i = 0; i < perService; i++) {
    const isLong = Math.random() < 0.4;
    const text = isLong ? pick(svc.long) : pick(svc.short);
    reviews.push({
      service: svc.key,
      name: randomName(),
      rating: randomRating(),
      text,
      date: randomDate(),
    });
  }
}

