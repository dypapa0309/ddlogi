function randomName() {
  const s = surnames[Math.floor(Math.random() * surnames.length)];
  const l = lastNames[Math.floor(Math.random() * lastNames.length)];
  return `${s}*${l}`;
}

function randomDate() {
  const year = Math.random() < 0.3 ? 2024 : Math.random() < 0.6 ? 2025 : 2026;
  let month;
  if (year === 2024) month = Math.floor(Math.random() * 9) + 4; // 2024-04 ~ 2024-12
  else if (year === 2025) month = Math.floor(Math.random() * 12) + 1;
  else month = Math.floor(Math.random() * 3) + 1; // 2026-01 ~ 2026-03
  return `${year}-${String(month).padStart(2, '0')}`;
}

function randomRating() {
  const r = Math.random();
  if (r < 0.72) return 5;
  if (r < 0.92) return 4;
  return 3;
}

function stars(n) {
  return '★'.repeat(n) + '☆'.repeat(5 - n);
}

